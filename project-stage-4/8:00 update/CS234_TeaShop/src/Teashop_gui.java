    private static void writeCustomerInfo(){
        try{         
            //update customer save file
            File customerFile = new File("customers.csv");
            BufferedWriter Cbf = new BufferedWriter(new FileWriter(customerFile));

            LinkedList<Customer> customers = Financial.getCustomers();
            for (int i=0;i<customers.size();i++){
                String row = "";
                Customer customer_i = customers.get(i);
                String cName = customer_i.getName();
                String cID = String.valueOf(customer_i.getID());
                String isMember = String.valueOf(customer_i.isMember());

                row = cName + "," + cID + "," +isMember + "\n";
                Cbf.write(row);
            }
            Cbf.close();
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null, "Error saving information.");
        }
        
    }
