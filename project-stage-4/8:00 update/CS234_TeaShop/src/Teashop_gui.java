    public static void readCustomerInfo(){
        //read in saved customers, items, etc.
        try{
            //read save info for customers
            File customerFile = new File("customers.csv");
            FileReader Cfr = new FileReader(customerFile);
            BufferedReader Cbr = new BufferedReader(Cfr);
            
            String line = "";
            String[] c;
            while ((line = Cbr.readLine()) != null){
                c = line.split(",");
                String name = c[0];
                int id = Integer.parseInt(c[1]);
                boolean b = Boolean.parseBoolean(c[2]);
                Customer newCustomer = new Customer(name, id, b);
                Financial.addCustomer(newCustomer);
            }
            Cbr.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, "There was an error reading saved information.");
        }
    }
